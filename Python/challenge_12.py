'''
Challenge 12 - Multiplication

Given the average amount spent on lunch by student per day, calculate the total
spending in the week. Assume that the student spends the same amount on food 
each day
'''

daily_spending = float(input("How much do you spend on lunch on an average day? \n"))
weekly_spending = daily_spending * 5
print ("This means that in a week you will spend", weekly_spending, " on food")
