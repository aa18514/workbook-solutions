mood_level = input("Rate your mood from 1 to 10 where 1 is very sad and 10 is extremely happy")
if mood_level <= 3:
    print('Cheer up, tomorrow is a new day')
if mood_level > 3 and mood_level <= 7:
    print('Its okay, everyone has average days')
if mood_level > 7 and mood_level <= 10:
    print('Awesome, it is great that you are so happy')


    
